import re

def process():
    changed = []
    for filepath, repls in replacements_by_file.items():
        try:
            with open(filepath, 'r', encoding='utf-8') as f:
                content = f.read()
        except Exception as e:
            print(f"Error reading {filepath}: {e}")
            continue
        
        original = content
        for old, new in repls:
            if old in content:
                content = content.replace(old, new)
                print(f"  {filepath}: replaced '{old[:50]}...'")
        
        if content != original:
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(content)
            changed.append(filepath)
    
    print(f"\nChanged {len(changed)} files")
    
    # Quick check remaining
    print("\n=== Remaining visible Chinese [TEXT] ===")
    import glob
    chinese = re.compile(r'[\u4e00-\u9fff]+')
    for f in sorted(glob.glob("frontend/src/**/*.vue", recursive=True)):
        try:
            with open(f, 'r', encoding='utf-8') as fh:
                for i, line in enumerate(fh, 1):
                    if chinese.search(line):
                        s = line.strip()
                        if not (s.startswith('//') or s.startswith('/*') or s.startswith('*') or s.startswith('<!--')):
                            matches = chinese.findall(line)
                            print(f"  {f}:{i} {''.join(matches)}")
        except:
            pass

process()
