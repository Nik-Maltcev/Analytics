"""
Translate LLM prompts in profile generator and config generator to produce Russian output.
Key change: instruct LLM to output in Russian instead of Chinese.
"""

def fix(filepath, replacements):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    original = content
    for old, new in replacements:
        if old in content:
            content = content.replace(old, new)
            print(f"  {filepath}: OK")
        else:
            print(f"  {filepath}: MISS '{old[:60]}'")
    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)

