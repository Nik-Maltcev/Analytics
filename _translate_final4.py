"""
Final translation pass - fix all remaining Chinese text line by line.
Read exact lines, replace precisely.
"""
import re

def fix_lines(filepath, fixes):
    """fixes: list of (line_num, old_substring, new_substring)"""
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    
    changed = False
    for line_num, old, new in fixes:
        idx = line_num - 1
        # Search in range ±5
        for offset in range(-5, 6):
            check = idx + offset
            if 0 <= check < len(lines) and old in lines[check]:
                lines[check] = lines[check].replace(old, new)
                print(f"  L{check+1}: OK '{old[:50]}'")
                changed = True
                break
        else:
            print(f"  L{line_num}: MISS '{old[:50]}'")
    
    if changed:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.writelines(lines)
    return changed

